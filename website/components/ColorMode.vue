  ----------------------------------------------------------------------- -->
<script>
export default {
    name: 'ColorMode',
    data() {
        return {
            themeColors: ['system', 'light', 'dark', 'sepia']
        }
    },
    methods: {
        toggleColorMode(color) {
            const modes = {
                'light': 'dark',
                'dark': 'sepia',
                'sepia': 'light'
            }
            if (color in modes) {
                this.$colorMode.preference = modes[color]
            }
            else {
                this.$colorMode.preference = this.$colorMode.value
            }
        }
    }
}
</script>
